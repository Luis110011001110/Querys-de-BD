#CONSULTA HAVING
SELECT  DEPARTMENT_ID, AVG(SALARY)
FROM HR.EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING MAX(SALARY)<5000;

#CONSULTA ORDER BY
SELECT DEPARTMENT_ID, AVG(SALARY)
FROM SALARY
WHERE SALARY >=5000;
ORDER BY SALARY [ASC | DESC ];


#CONSULTA DE EMPLEADOS QUE NO TIENE DEPARTAMENTO
SELECT COUNT (EMPLOYEE_ID)
FROM HR.EMPLOYEES
WHERE DEPARTMENT_ID IS NULL;

#NUMERO DE REGISTROS DE LA CONSULTA JOIN
SELECT COUNT(em.EMPLOYEE_ID)
FROM HR.EMPLOYEES em.DEPARTAMENTS dep
WHERE em.DEPARTMENT_ID - dep.DEPARTMENT_ID;

#NUMERO DE EMPLEADOS EN EMPLOYEES
SELECT COUNT ( EMPLOYEE_ID)
FROM HR.EMPLOYEES;

#CONSULTA COMUN EN EMPLOYEES
SELECT em.EMPLOYEE_ID
		,em.LAST_NAME
		,em.SALARY
		,em.COMMISION_PCT
		,em.DEPARTMENT_ID
		,dep.DEPARTMENT_NAME AS NOMBRE_DEPTO
FROM HR.EMPLOYEES em, HR.DEPARTAMENTS dep
WHERE em.DEPARTMENT_ID = dep.DEPARTMENT_ID;

#natural join
SELECT DEPARTMENT_ID
		,DEPARTMENT_NAME
		,LOCATION_ID
		,CITY
FROM DEPARTAMENTS
NATURAL JOIN LOCATIONS;


#natural join con condicion
SELECT DEPARTMENT_ID
		,DP.DEPARTMENT_NAME
		,DP.LOCATION_ID
		,DP.CITY
FROM DEPARTAMENTS DP, LOCATION LO
WHERE LO.LOCATION_ID = DP.LOCATION_ID;


